import { execFileSync } from 'node:child_process';
import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const dataDir = join(root, 'data/paper-dolls');
const schemaPath = join(root, 'schemas/paper-dolls/paper-dolls.schema.json');
const assetsRoot = join(root, 'assets/paper-dolls');
const publicDir = join(root, 'packages/web/public/paper-dolls');
const outputPath = join(root, 'packages/core/src/generated/paper-dolls.ts');
const prepareScript = join(root, 'scripts/prepare-unit-sprite.py');

const ajvPath = join(
  root,
  'node_modules/.pnpm/@redocly+ajv@8.11.2/node_modules/@redocly/ajv/dist/2020.js',
);
const { default: Ajv } = await import(pathToFileURL(ajvPath).href);

const schema = JSON.parse(readFileSync(schemaPath, 'utf8'));
const ajv = new Ajv({ allErrors: true, strict: false });
const validate = ajv.compile(schema);

const spriteFiles = readdirSync(dataDir)
  .filter(
    (name) =>
      name.endsWith('.json') &&
      !name.includes('-img2img') &&
      name !== 'archetypes.json',
  )
  .sort();

if (spriteFiles.length === 0) {
  throw new Error(`No unit sprites in ${dataDir}`);
}

const sprites = spriteFiles.map((name) => {
  const path = join(dataDir, name);
  return JSON.parse(readFileSync(path, 'utf8'));
});

if (!validate(sprites)) {
  const details = validate.errors?.map((error) => `${error.instancePath} ${error.message}`).join('; ');
  throw new Error(`Invalid unit sprite catalog: ${details}`);
}

mkdirSync(publicDir, { recursive: true });
const spriteMeta = {};

for (const sprite of sprites) {
  const srcPath = join(assetsRoot, sprite.sprite);
  const packed = JSON.parse(
    execFileSync('python3', [prepareScript, join(dataDir, `${sprite.id}.json`), srcPath, publicDir], {
      encoding: 'utf8',
    }),
  );
  spriteMeta[sprite.id] = packed;
}

const serializedSprites = JSON.stringify(sprites, null, 2);
const serializedMeta = JSON.stringify(spriteMeta, null, 2);

const output = `/* eslint-disable */
/** Generated by scripts/compile-paper-dolls.mjs — do not edit. */
export const PAPER_DOLL_RIGS = ${serializedSprites} as const;

export type PaperDollRigId = (typeof PAPER_DOLL_RIGS)[number]['id'];

export const PAPER_DOLL_ATLAS = ${serializedMeta} as const;

export const PAPER_DOLL_BY_UNIT: Record<string, (typeof PAPER_DOLL_RIGS)[number]> = Object.fromEntries(
  PAPER_DOLL_RIGS.map((sprite) => [sprite.unitId, sprite]),
);

export const PAPER_DOLL_CANVAS_SIZE = 512;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output, 'utf8');
console.log(`Compiled ${sprites.length} unit sprite(s) to ${outputPath}`);
