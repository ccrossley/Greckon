import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { basename, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const unitsPath = join(root, 'data/units.json');
const iconsDir = join(root, 'assets/unit-icons');
const outputPath = join(root, 'packages/core/src/generated/unit-icons.ts');

const ICON_SIZE = 512;

function extractPaths(svgBody) {
  const paths = [];
  for (const match of svgBody.matchAll(/\sd="([^"]+)"/g)) {
    paths.push(match[1]);
  }
  if (paths.length === 0) {
    throw new Error('No path data found in icon SVG');
  }
  return paths.join(' ');
}

async function fetchIconSvg(slug) {
  const response = await fetch(`https://api.iconify.design/game-icons.json?icons=${encodeURIComponent(slug)}`);
  if (!response.ok) {
    throw new Error(`Iconify request failed for ${slug} (${response.status})`);
  }
  const payload = await response.json();
  const icon = payload.icons?.[slug];
  if (!icon?.body) {
    throw new Error(`Unknown game-icons slug: ${slug}`);
  }
  const pathData = extractPaths(icon.body);
  const svg = [
    `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${ICON_SIZE} ${ICON_SIZE}">`,
    `  <path d="${pathData}"/>`,
    '</svg>',
  ].join('\n');
  return { pathData, svg };
}

async function loadIcon(slug) {
  mkdirSync(iconsDir, { recursive: true });
  const filePath = join(iconsDir, `${slug}.svg`);

  if (!existsSync(filePath)) {
    const fetched = await fetchIconSvg(slug);
    writeFileSync(filePath, fetched.svg, 'utf8');
    return fetched.pathData;
  }

  const svg = readFileSync(filePath, 'utf8');
  return extractPaths(svg);
}

const units = JSON.parse(readFileSync(unitsPath, 'utf8'));
const slugPaths = {};

for (const unit of units) {
  if (!unit.icon || typeof unit.icon !== 'string') {
    throw new Error(`Unit ${unit.id} is missing required "icon" (game-icons slug)`);
  }
  slugPaths[unit.icon] = await loadIcon(unit.icon);
}

mkdirSync(iconsDir, { recursive: true });
for (const fileName of readdirSync(iconsDir)) {
  if (!fileName.endsWith('.svg')) {
    continue;
  }
  const slug = basename(fileName, '.svg');
  if (!slugPaths[slug]) {
    slugPaths[slug] = extractPaths(readFileSync(join(iconsDir, fileName), 'utf8'));
  }
}

const unitIconPaths = Object.fromEntries(
  units.map((unit) => [unit.id, slugPaths[unit.icon]]),
);

const output = `/* eslint-disable */
/** Generated by scripts/compile-unit-icons.mjs — do not edit. */
import type { UnitType } from './units.js';

export const UNIT_ICON_SIZE = ${ICON_SIZE};

export const ICON_SLUG_PATHS: Record<string, string> = ${JSON.stringify(slugPaths, null, 2)};

export const UNIT_ICON_PATHS: Record<UnitType, string> = ${JSON.stringify(unitIconPaths, null, 2)} as Record<UnitType, string>;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output, 'utf8');
console.log(
  `Compiled ${Object.keys(unitIconPaths).length} unit icons (${Object.keys(slugPaths).length} slugs) to ${outputPath}`,
);
