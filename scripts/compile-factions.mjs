import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const factionsPath = join(root, 'data/factions.json');
const schemaPath = join(root, 'schemas/factions/factions.schema.json');
const outputPath = join(root, 'packages/core/src/generated/factions.ts');

const ajvPath = join(
  root,
  'node_modules/.pnpm/@redocly+ajv@8.11.2/node_modules/@redocly/ajv/dist/2020.js',
);
const { default: Ajv } = await import(pathToFileURL(ajvPath).href);

const schema = JSON.parse(readFileSync(schemaPath, 'utf8'));
const ajv = new Ajv({ allErrors: true, strict: false });
const validate = ajv.compile(schema);

const factions = JSON.parse(readFileSync(factionsPath, 'utf8'));
if (!Array.isArray(factions)) {
  throw new Error(`${factionsPath} must be a JSON array of faction definitions`);
}

if (!validate(factions)) {
  const details = validate.errors?.map((error) => `${error.instancePath} ${error.message}`).join('; ');
  throw new Error(`Invalid faction catalog: ${details}`);
}

const ids = new Set(factions.map((faction) => faction.id));
if (ids.size !== factions.length) {
  throw new Error(`Duplicate faction ids in ${factionsPath}`);
}

if (factions.length === 0) {
  throw new Error(`No factions defined in ${factionsPath}`);
}

const serialized = JSON.stringify(factions, null, 2);
const output = `/* eslint-disable */
/** Generated by scripts/compile-factions.mjs — do not edit. */
export const FACTIONS = ${serialized} as const;

export type FactionId = (typeof FACTIONS)[number]['id'];

export type Faction = (typeof FACTIONS)[number];

export const FACTION_CATALOG: Record<FactionId, Faction> = Object.fromEntries(
  FACTIONS.map((faction) => [faction.id, faction]),
) as Record<FactionId, Faction>;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output, 'utf8');
console.log(`Compiled ${factions.length} factions from ${factionsPath} to ${outputPath}`);
