import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const abilitiesPath = join(root, 'data/abilities.json');
const schemaPath = join(root, 'schemas/abilities/abilities.schema.json');
const outputPath = join(root, 'packages/core/src/generated/abilities.ts');

const ajvPath = join(
  root,
  'node_modules/.pnpm/@redocly+ajv@8.11.2/node_modules/@redocly/ajv/dist/2020.js',
);
const { default: Ajv } = await import(pathToFileURL(ajvPath).href);

const schema = JSON.parse(readFileSync(schemaPath, 'utf8'));
const ajv = new Ajv({ allErrors: true, strict: false });
const validate = ajv.compile(schema);

const abilities = JSON.parse(readFileSync(abilitiesPath, 'utf8'));
if (!Array.isArray(abilities)) {
  throw new Error(`${abilitiesPath} must be a JSON array of ability definitions`);
}

if (!validate(abilities)) {
  const details = validate.errors?.map((error) => `${error.instancePath} ${error.message}`).join('; ');
  throw new Error(`Invalid ability catalog: ${details}`);
}

const ids = new Set(abilities.map((ability) => ability.id));
if (ids.size !== abilities.length) {
  throw new Error(`Duplicate ability ids in ${abilitiesPath}`);
}

const serialized = JSON.stringify(abilities, null, 2);
const output = `/* eslint-disable */
/** Generated by scripts/compile-abilities.mjs — do not edit. */
export const ABILITIES = ${serialized} as const;

export type AbilityId = (typeof ABILITIES)[number]['id'];

export type Ability = (typeof ABILITIES)[number];

export type TargetingMode = Ability['targeting'];

export type EffectKind = Ability['effects'][number]['kind'];

export const ABILITY_CATALOG: Record<AbilityId, Ability> = Object.fromEntries(
  ABILITIES.map((ability) => [ability.id, ability]),
) as Record<AbilityId, Ability>;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output, 'utf8');
console.log(`Compiled ${abilities.length} abilities from ${abilitiesPath} to ${outputPath}`);
